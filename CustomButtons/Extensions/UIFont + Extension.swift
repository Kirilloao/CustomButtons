import UIKit

extension UIFont {

    static func rubickMedium(ofSize: CGFloat) -> UIFont? {
        return UIFont(name: "Rubik-Medium", size: ofSize)
    }

    static func rubickRegular(ofSize: CGFloat) -> UIFont? {
        return UIFont(name: "Rubik-Regular", size: ofSize)
    }
}
